num1 = int(input("Give me the first number: "))
num2 = int(input("Give me the second number: "))
print("Thank you!")

plus = num1 + num2
minus = num1 - num2
divide = num1 // num2
multi = num1 * num2

print(f"{num1} + {num2} = {plus}")
print(f"{num1} - {num2} = {minus}")
print(f"{num1} / {num2} = {divide}")
print(f"{num1} * {num2} = {multi}")